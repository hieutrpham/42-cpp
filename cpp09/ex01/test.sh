#!/usr/bin/env bash

test=(
	"1 + 1 - -"
	"-1* 1 -"
	"0 0 /"
	"9 9 9 9 9 9 9 9 9 9 * * * * * * * * *"
	"1 10y *"
	"1.a 10 *"
	"0.2 1 /"
	"-12 1 *"
	"-1 1 *"
	"8 9 * 9 - 9 - 9 - 4 - 1 +"
	"7 7 * 7 -"
	"1 2 * 2 / 2 * 2 4 - +"
	"(1 + 1)"
	"-12 * 2 -"
 )

for item in "${test[@]}"; do
	echo -n "[$item] : "
	./RPN "$item"
done
